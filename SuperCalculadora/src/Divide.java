
public class Divide {
	//MÉTODO DIVIDIR
		public void dividir(double x, double y) {
			double dividir, resto;
			dividir = x / y;
			resto = x % y;
			System.out.println("\nO resultado inteiro da divisão é: "+x+" / "+y+" = "+dividir);
			System.out.println("\nO resultado do resto da divisão é: "+x+" / "+y+" = "+resto+"\n");

		}
}
